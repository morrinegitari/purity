#Arithmetic operators
x=10
y=5
print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y) #this is modulus -returns the remainder

#comparison operators
print(x < y)
print(x > y)
print(x <= y)
print(x >= y)
print(x == y)# equal to
print(x != y)# not equal to

#Assignment operators
a =30
print(a)

a -= 2
print(a)

#Operator precedence
number= (45-2*3+5)
print(number)

#logical operators-and,or ,not
print(55<234 and 67<23)
print(55<234 or 67<23)
print(not (55<234 or 67<23))